// Regenerates app/icons-manifest.ts from icons/*.tsx (site-only, not in registry).
import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const root = path.join(import.meta.dirname, '..');
const files = (await readdir(path.join(root, 'icons')))
  .filter((f) => f.endsWith('.tsx'))
  .sort();

const entries = [];
for (const f of files) {
  const src = await readFile(path.join(root, 'icons', f), 'utf8');
  const m = src.match(/export \{ (\w+) \};/);
  if (!m) continue;
  entries.push({ name: f.replace('.tsx', ''), component: m[1] });
}

const out = `// Generated by scripts/gen-manifest.mjs — do not edit.
${entries.map((e) => `import { ${e.component} } from '@/icons/${e.name}';`).join('\n')}

export const ICON_LIST = [
${entries.map((e) => `  { name: '${e.name}', Icon: ${e.component} },`).join('\n')}
];
`;

await writeFile(path.join(root, 'app', 'icons-manifest.ts'), out);
console.log(`✓ app/icons-manifest.ts (${entries.length} icons)`);
